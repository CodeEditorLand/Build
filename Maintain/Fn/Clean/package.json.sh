Length=${#Exclude[@]}
Size=210

Key() {
	JSON=$(< package.json)

	for ((Start = 0; Start < Length; Start += Size)); do
		Chunk=("${Exclude[@]:Start:Size}")

		JQ="del("

		for Exclude in "${Chunk[@]}"; do
			JQ+=".[\"$1\"].[\"${Exclude}\"],"
		done

		JQ=${JQ%,}

		JQ+=")"

		JSON=$(echo "$JSON" | \jq -S --tab "$JQ")

		echo "$JSON" >| package.json
	done
}

Key "dependencies"
Key "devDependencies"
Key "peerDependencies"
Key "optionalDependencies"
